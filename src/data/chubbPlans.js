function planImagePath(id) {
  return `assets/plans/${id}.png`;
}

function createPlan({
  id,
  name,
  category,
  provider = 'Chubb Life',
  badge,
  highlight,
  description,
  benefits,
  targetAudience,
  brochureSource,
  featured = false,
  featuredOrder,
  rating = 4.7,
}) {
  return {
    id,
    slug: id,
    name,
    displayNameTh: name,
    category,
    provider,
    badge,
    monthlyPremium: 0,
    annualPremium: 0,
    coverageAmount: 0,
    rating,
    featured,
    featuredOrder,
    highlight,
    image: planImagePath(id),
    description,
    benefits,
    targetAudience,
    waitingPeriod: 'ตามเงื่อนไขกรมธรรม์',
    brochurePdfs: [],
    brochureUrl: brochureSource ? `assets/brochures/${brochureSource}/index.html` : '',
    pricingTables: [],
  };
}

